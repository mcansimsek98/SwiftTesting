import Foundation

class MatematicFunctions {
    func addIntegers(x: Int, y: Int) -> Int {
        return x + y
    }
    
    func multiplyNumbers(x: Int, y: Int) -> Int {
        return x * y
    }
    
    func divideIntegers(x: Int, y: Int) -> Int {
        return x / y
    }
}
